                if ($nextBtn.length === 0) {
                    $nextBtn = $('<div class="swiper-button-next secondary-featured-next-new"></div>');
                    $('#viewAllnew').append($nextBtn);
                }
                if ($prevBtn.length === 0) {
                    $prevBtn = $('<div class="swiper-button-prev secondary-featured-prev-new"></div>');
                    $('#viewAllnew').append($prevBtn);
