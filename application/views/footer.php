        $('.flat-video-reels .video-item').on('click', function (e) {
            // Don't trigger if clicking on video/iframe controls
            if ($(e.target).is('video') || $(e.target).closest('video').length ||
                $(e.target).is('iframe') || $(e.target).closest('iframe').length) {
                return;
            }

            const $videoWrapper = $(this).find('.video-wrapper-reel');
            const $video = $videoWrapper.find('video');
            const $iframe = $videoWrapper.find('iframe');
            const $playBtn = $videoWrapper.find('.play-btn');
            const isYouTube = $(this).data('is-youtube') === true || $(this).data('is-youtube') === 'true';

            // Stop all other videos in this section
            $('.flat-video-reels .video-wrapper-reel').not($videoWrapper).each(function () {
                const $otherVideo = $(this).find('video')[0];
                const $otherIframe = $(this).find('iframe');
                if ($otherVideo) {
                    $otherVideo.pause();
                    $otherVideo.currentTime = 0;
                }
                // For YouTube iframes, remove src to stop playback
                if ($otherIframe.length) {
                    $otherIframe.attr('src', '');
                }
                $(this).removeClass('playing');
            });

            // Toggle current video
            if ($videoWrapper.hasClass('playing')) {
                // Pause video
                if ($video[0]) {
                    $video[0].pause();
                }
                // For YouTube iframes, remove src to stop playback
                if ($iframe.length) {
                    $iframe.attr('src', '');
                }
                $videoWrapper.removeClass('playing');
            } else {
                // Play video
                if (isYouTube && $iframe.length) {
                    // For YouTube, set the embed URL with autoplay
                    const embedUrl = $iframe.data('embed-url');
                    if (embedUrl) {
                        // Hide thumbnail and play button
                        $videoWrapper.find('img').css('display', 'none');
                        $playBtn.css('display', 'none');

                        // Add autoplay parameter
                        const autoplayUrl = embedUrl + (embedUrl.indexOf('?') > -1 ? '&' : '?') + 'autoplay=1&rel=0';
                        $iframe.attr('src', autoplayUrl);
                        // Force iframe to be visible
                        $iframe.css({
                            'display': 'block',
                            'visibility': 'visible',
                            'opacity': '1',
                            'z-index': '3'
                        });
                    }
                } else if ($video[0]) {
                    // Hide thumbnail and play button for regular videos
                    $videoWrapper.find('img').css('display', 'none');
                    $playBtn.css('display', 'none');
                    $video.css('display', 'block');
                    $video[0].play().catch(function (error) {
                        console.log('Video play error:', error);
                    });
                }
                $videoWrapper.addClass('playing');
            }
        });

        // Pause video when it ends (for HTML5 videos only)
