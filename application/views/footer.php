            if (!window._googleAuthInitialized) {
                google.accounts.id.initialize({
                    client_id: clientId,
                    auto_select: true,
                    cancel_on_tap_outside: false,
                    ux_mode: 'popup',
                    callback: function (response) {
                        if (!response || !response.credential) {
                            alert('Google sign-in failed. Please try again.');
                            return;
                        }

                        // Decode ID token payload (email, name, picture)
                        const tokenParts = response.credential.split('.');
                        if (tokenParts.length < 2) {
                            alert('Invalid Google response. Please try again.');
                            return;
                        }

                        let payload = {};
                        try {
                            payload = JSON.parse(atob(tokenParts[1].replace(/-/g, '+').replace(/_/g, '/')));
                        } catch (e) {
                            console.error('Failed to decode Google token:', e);
                        }

                        const user = {
                            email: payload.email || '',
                            displayName: payload.name || payload.email || 'Google User',
                            photoURL: payload.picture || ''
                        };

                        if (!user.email) {
                            alert('Google account email not available.');
                            return;
                        }

                        handleGoogleSignInSuccess(response.credential, user);
                    }
                });
                window._googleAuthInitialized = true;
            }

            google.accounts.id.prompt();
