    // Submit enquiry to server - No login required
    function submitEnquiry(propertyData, message, name, email, phone, city) {
        const submitBtn = $('.enquiry-btn[data-property-id="' + propertyData.propertyId + '"]');
        const originalText = submitBtn.find('span').text();

        // Show loading state
        submitBtn.find('span').html('<span style="display: inline-block; width: 14px; height: 14px; border: 2px solid white; border-top-color: transparent; border-radius: 50%; animation: spin 0.6s linear infinite; margin-right: 8px;"></span> Sending...');
        submitBtn.prop('disabled', true);

        const formData = new FormData();
        formData.append('property_id', propertyData.propertyId);
        formData.append('name', name || '');
        formData.append('email', email || '');
        formData.append('phone', phone || '');
        formData.append('city', city || '');
        formData.append('message', message || '');

        fetch('<?php echo base_url("api/enquiry/store"); ?>', {
            method: 'POST',
            body: formData
        })
            .then(response => response.json())
            .then(data => {
                if (data.success) {
                    submitBtn.find('span').text('Enquiry Sent');
                    submitBtn.css({
                        'background': 'linear-gradient(135deg, #28a745 0%, #20c997 100%)',
                        'border': '2px solid #28a745',
                        'pointer-events': 'none'
                    });
                    if (typeof showSuccessPopup === 'function') {
                        showSuccessPopup(data.message || 'Enquiry submitted successfully!');
                        setTimeout(() => {
                            if (typeof closeSuccessPopup === 'function') {
                                closeSuccessPopup();
                            }
                        }, 3000);
                    } else {
                        alert(data.message || 'Enquiry submitted successfully!');
                    }
                } else {
                    alert(data.message || 'Failed to send enquiry. Please try again.');
                    submitBtn.find('span').text(originalText);
                    submitBtn.prop('disabled', false);
                }
            })
            .catch(error => {
                console.error('Enquiry error:', error);
                alert('An error occurred while sending enquiry. Please try again.');
                submitBtn.find('span').text(originalText);
                submitBtn.prop('disabled', false);
            });
    }

