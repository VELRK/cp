            if ($wrapper.length) {
                const $video = $wrapper.find('video')[0];
                const $iframe = $wrapper.find('iframe');
                const $img = $wrapper.find('img');
                const $playBtn = $wrapper.find('.play-btn');

                // Pause video
                if ($video) {
                    $video.pause();
                    $video.currentTime = 0;
                    $video.style.display = 'none';
                }

                // Stop YouTube iframe
                if ($iframe.length) {
                    $iframe.attr('src', '');
                    $iframe.css({
                        'display': 'none',
                        'visibility': 'hidden',
                        'opacity': '0',
                        'z-index': '-1'
                    });
                }

                // Show thumbnail and play button
                if ($img.length) {
                    $img.css('display', 'block');
                }
                if ($playBtn.length) {
                    $playBtn.css('display', 'flex');
                }

                // Remove playing class
                $wrapper.removeClass('playing');
            }
        });

        // Handle stop button clicks for Properties Reels
        $(document).on('click', '.video-wrapper .stop-button-video-reel', function (e) {
            e.preventDefault();
            e.stopPropagation();

            const wrapperId = $(this).data('video-wrapper');
            const $wrapper = $('#' + wrapperId);

            if ($wrapper.length) {
                const $video = $wrapper.find('video')[0];
                const $img = $wrapper.find('img');
                const $playBtn = $wrapper.find('.play-button');

                // Pause video
                if ($video) {
                    $video.pause();
                    $video.currentTime = 0;
                    $video.style.display = 'none';
                }

                // Show thumbnail and play button
                if ($img.length) {
                    $img.css('display', 'block');
                }
                if ($playBtn.length) {
                    $playBtn.css('display', 'flex');
                }

                // Remove playing class
                $wrapper.removeClass('playing');

                // Also remove playing class from parent flat-blog-item
                $wrapper.closest('.flat-blog-item').removeClass('playing');
            }
        });
    });

