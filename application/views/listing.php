                            var footerBottom = footer.offsetTop + footer.offsetHeight;
                            var scrollHeight = Math.max(
                                body.scrollHeight,
                                html.scrollHeight
                            );
                            
                            // If there's significant white space (more than 100px), fix it
                            var extraSpace = scrollHeight - footerBottom;
                            if (extraSpace > 100) {
                                // Set body and html height to match actual content
                                var actualContentHeight = footerBottom;
                                body.style.minHeight = actualContentHeight + 'px';
                                html.style.minHeight = actualContentHeight + 'px';
                                
                                // Also ensure no overflow
